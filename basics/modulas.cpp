#include <iostream>
using namespace std;
int main(){
  int x=23,y=5;
  cout << "The remainder of " << x << ", "<< y << " will be " << x%y << "." << endl ;
  int a=2,b=8;
  cout << "The remainder of " << a << ", "<< b << " will be " << a%b << "." << endl ;
  int c=10,d=10;
  cout << "The remainder of " << c << ", "<< d << " will be " << c%d << "." << endl ;
  int e=12,f=-5;
  cout << "The remainder of " << e << ", "<< f << " will be " << e%f << "." << endl ;
  int g=-12,h=7;
  cout << "The remainder of " << g << ", "<< h << " will be " << g%h << "." << endl ;
  return 0;
}
